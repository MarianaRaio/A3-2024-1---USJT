    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        
        int linhaTabela = tblODS.getSelectedRow();
        if (linhaTabela != -1) {
            int id_projeto = Integer.parseInt(tblODS.getValueAt(linhaTabela, 0).toString());
            String descricao = tblODS.getValueAt(linhaTabela, 2).toString();
            int id_descricao = subDao.buscarId(descricao);
            String descricao_proj = tblODS.getValueAt(linhaTabela, 4).toString();
            
            TelaGerenciamentoProjetos tela = new TelaGerenciamentoProjetos(id_projeto, id_descricao, permissao, descricao_proj);
            tela.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para atualizar!");
        }


    }//GEN-LAST:event_btnAtualizarActionPerformed

