    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        int linhaTabela = tblODS.getSelectedRow();
        int id_subprojetos, id_projetos;

        if (linhaTabela != -1) {
            id_projetos = Integer.parseInt(tblODS.getValueAt(linhaTabela, 0).toString());
            id_subprojetos = subDao.buscarId(tblODS.getValueAt(linhaTabela, 2).toString());
            dao.excluirDadosTabela(id_projetos, id_subprojetos);
            JOptionPane.showMessageDialog(null, "Projeto excluido com sucesso!");

            preencherTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione uma linha para excluir");
        }
            
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String termoBuscar = txtTermoBuscar.getText();
        String idBuscar = (txtId.getText());

        if (termoBuscar.isEmpty() || idBuscar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite o nome da ODS para buscar");

            return;
        }

        List<Object[]> data = dao.buscarPorNome(termoBuscar, Integer.parseInt(idBuscar));

        DefaultTableModel model = (DefaultTableModel) tblODS.getModel();
        model.setRowCount(0); // Limpa a tabela antes de preencher

        for (Object[] row : data) {
            model.addRow(row);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

